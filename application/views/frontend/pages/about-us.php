                               <p id="text1">
Pakistan Society of Internal Medicine began with a dream of a young physician. An idea envisaged first of all by a physician who has spent her life thinking of new avenues to explore in her beloved field. She floated an idea about a unified platform for internists, and the massive response on the idea exhilarated her to practically put it to action. She put it up as her Facebook status and after getting enormous positive response from physicians in her social circle, she said let us do it, and went to none other than the leader of the physicians Prof. Dr. Javed Akram, who gathered like-minded people, all shining stars from the galaxy of medicine to brighten this idea and make this dream a reality. They all gathered for brain storming to devise methodology for putting life in an idea. Planning first? Or start with the registrations foremost? But one thing that they all believed in simultaneously and unanimously was the desire; we can do it, we will do it. They began with the use of technology and formed a group on WhatsApp for physicians of Pakistan and within first 24 hours of the creation of the Pakistan Society Of Internal Medicine WhatsApp group, the group number exceeded the capacity of the platform, being joined in multitudes by physicians from Pakistan and abroad simultaneously. This is how the birth of Pakistan Society of Internal Medicine took place, a moment of pride for physicians all around. <br>
PSIM was formally launched on 16th January 2019 with an aim to enhance the quality and effectiveness of healthcare by fostering excellence and professionalism in the practice of medicine and within a short span of three and a half years its being recognized as leading medical society contributing to quality patient care, health advocacy, medical education and mentoring of young physicians. With its footprint in all corners of Pakistan in the form of its active and vibrant city chapters and even recognition among overseas Pakistanis, PSIM has become the fastest growing medical organization. The society continues to collaborate with many organizations at national and international level in different avenues of medical profession and brings glory to the field of internal medicine which is the mother of all medical specialties. <br>
I wish to see the society outshining in all the future ventures as sky is the limit.


